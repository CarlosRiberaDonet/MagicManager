    public void loadCardDetails(Card card){
        
        cantjLabel.setText(String.valueOf(getUserCardCount()));
        loadImage(card.getImageUrl());
        nombre.setText(card.getName());
        rareza.setText(card.getRarity());
        numColeccionista.setText(card.getCollector_number());
        edicion.setText(card.getSet_name());
        idioma.setText(card.getLang());
        valorLabel.setText(card.getEurPrice());
        valorFoilLabel.setText(card.getEurPriceFoil());
    }
    
    // Método para cargar la imagen de la carta a través de la url
    public void loadImage(String urlImage){
        try {
            URL url = new URL(card.getImageUrl()); // método personalizado que devuelve la URL
            ImageIcon icon = new ImageIcon(url);
            Image scaled = icon.getImage().getScaledInstance(223, 310, Image.SCALE_SMOOTH); // ajusta tamaño
            imageLabel.setIcon(new ImageIcon(scaled));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    // Método para contar las copias que tiene el USR de una misma carta
    public int getUserCardCount(){
        Card userCard = UserController.cardMap.get(card.getCardId());
        
        // Si el userMap contiene la carta
        if(userCard != null){
            return userCard.getCardCount();
        }
        return 0;
    }

