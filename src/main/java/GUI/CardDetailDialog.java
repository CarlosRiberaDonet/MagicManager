    // Método para abrir el link de la carta
    private void linkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_linkButtonActionPerformed
        String link = card.getNewUrl();
        // Abrir la URL si no es nula
        if (link != null && !link.isEmpty()) {
            MisMetodos.abrirURL(link);
        } else {
            JOptionPane.showMessageDialog(this, "La URL de CardMarket no está disponible.", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_linkButtonActionPerformed
